export class User {
  constructor(
    username,
    password,
    age,
    city,
    firstName,
    lastName,
    email,
    phone,
    role
  ) {
    this.username = username;
    this.password = password;
    this.age = age;
    this.city = city;
    this.firstName = firstName;
    this.lastName = lastName;
    this.email = email;
    this.phone = phone;
    this.role = role;
  }
}
